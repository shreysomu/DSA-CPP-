#include<iostream>
using namespace std;

int main(){
  int a = 5;
  float b = 4.5f;
  double d = 45.99;
  char ch = 'q';
  long long l = 5678765L;
  cout<<"Size of a is :"<<sizeof(a)<<endl;
  cout<<"Size of b is :"<<sizeof(b)<<endl;
  cout<<"Size of d is :"<<sizeof(d)<<endl;
  cout<<"Size of ch is :"<<sizeof(ch)<<endl;
  cout<<"Size of l is :"<<sizeof(l)<<endl;

}